export function getWorldDefinition(worldId: WorldId, customPrompt: string): WorldDefinition {
  if (worldId !== 'custom') return worlds[worldId];
  const base = worlds.custom;
  const prompt = customPrompt.trim();
  if (!prompt) return base;
  return {
    ...base,
    headline: `你定义的世界：${prompt}`,
    description: `当前采用本地探索模板承载该世界：${prompt}`,
    detailSections: [
      { title: '你的描述', content: prompt },
      ...base.detailSections.slice(0, 2),
    ],
    keywords: [prompt, ...base.keywords].slice(0, 4),
  };
}
